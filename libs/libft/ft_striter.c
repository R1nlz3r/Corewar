#include "libft.h"

void		ft_striter(char *s, void (*f)(char *))
{
	int		count;

	if (s != NULL && (*f) != NULL)
	{
		count = -1;
		while (s[++count] != '\0')
			(*f)(&s[count]);
	}
}
